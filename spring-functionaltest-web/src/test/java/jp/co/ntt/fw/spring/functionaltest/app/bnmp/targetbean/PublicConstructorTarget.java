package jp.co.ntt.fw.spring.functionaltest.app.bnmp.targetbean;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class PublicConstructorTarget {

    private int id;

    private String name;

    private static final Logger LOGGER = LoggerFactory.getLogger(
            PublicConstructorTarget.class);

    protected PublicConstructorTarget() {

    }

    public PublicConstructorTarget(int id, String name) {
        LOGGER.info("Only one public constructor was called !!");

        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
