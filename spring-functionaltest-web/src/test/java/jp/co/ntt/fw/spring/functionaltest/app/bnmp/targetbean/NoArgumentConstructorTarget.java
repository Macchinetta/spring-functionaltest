package jp.co.ntt.fw.spring.functionaltest.app.bnmp.targetbean;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class NoArgumentConstructorTarget {

    private int id;

    private String name;

    private static final Logger LOGGER = LoggerFactory.getLogger(
            NoArgumentConstructorTarget.class);

    public NoArgumentConstructorTarget() {

        LOGGER.info("No argument constructor was called !!");

    }

    public NoArgumentConstructorTarget(int id, String name) {

        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
